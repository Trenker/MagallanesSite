<h2><a name="upgrade"></a>Upgrade</h2>
<div>
    <p>
        Just as Install, Upgrading is very, very easy! Just run the <em>upgrade</em> command and Magallanes will auto-upgrade itself.
        <br />
        Also with <em>version</em> you can find out the current installed version.
    </p>

    <p class="bash">
        <span class="command-mage">mage</span> upgrade
    </p>

    <br />

    <p class="bash">
        <span class="command-mage">mage</span> version
    </p>

    <br />

</div>