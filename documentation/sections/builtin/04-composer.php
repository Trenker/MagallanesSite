<h2><a name="builtin-composer"></a>Composer</h2>
<div>
    <p>
    The following tasks are available for usage with <strong>Composer</strong>, by default the executable is presumed to be <em>php composer.phar</em> in the root directory:
    <ul>
        <li><strong>composer/generate-autoload</strong> - Generates de autload with the <em>optimize</em> flag.</li>
        <li><strong>composer/install</strong> - Installs the vendors.</li>
    </ul>
    </p>
</div>
